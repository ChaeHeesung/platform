package com.platform.library.dto;

public class RecommendVO {
	private int rid;
	private String gu;
	private String rlibrary;
	private String rbook;
	private String rwriter;
	private String rpublisher;
	private int ryear;
	private int rmon;
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getGu() {
		return gu;
	}
	public void setGu(String gu) {
		this.gu = gu;
	}
	public String getRlibrary() {
		return rlibrary;
	}
	public void setRlibrary(String rlibrary) {
		this.rlibrary = rlibrary;
	}
	public String getRbook() {
		return rbook;
	}
	public void setRbook(String rbook) {
		this.rbook = rbook;
	}
	public String getRwriter() {
		return rwriter;
	}
	public void setRwriter(String rwriter) {
		this.rwriter = rwriter;
	}
	public String getRpublisher() {
		return rpublisher;
	}
	public void setRpublisher(String rpublisher) {
		this.rpublisher = rpublisher;
	}
	public int getRyear() {
		return ryear;
	}
	public void setRyear(int ryear) {
		this.ryear = ryear;
	}
	public int getRmon() {
		return rmon;
	}
	public void setRmon(int rmon) {
		this.rmon = rmon;
	}
	
}
